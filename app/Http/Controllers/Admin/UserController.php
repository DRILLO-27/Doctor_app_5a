<?php

namespace App\Http\Controllers\Admin;

use Illuminate\Support\Facades\Auth;
use App\Http\Controllers\Controller;
use App\Models\Role;
use App\Models\User;
use Illuminate\Http\Request;

class UserController extends Controller
{
    /**
     * Muestra la lista de usuarios.
     */
    public function index()
    {
        return view('admin.users.index');
    }

    /**
     * Muestra el formulario para crear un nuevo usuario.
     */
    public function create()
    {
        $roles = Role::all();
        return view('admin.users.create', compact('roles'));
    }

    /**
     * Guarda un nuevo usuario
     */
    public function store(Request $request)
    {
        $data = $request->validate([
            'name' => 'required|string|min:3|max:255',
            'email' => 'required|string|email|unique:users',
            'password' => 'required|string|min:8|confirmed',
            'id_number' => 'required|string|min:5|max:20|regex:/^[A-Za-z0-9\-]+$/|unique:users',
            'phone' => 'required|digits_between:7,15', // CORREGIDO
            'address' => 'required|string|min:3|max:255',
            'role_id' => 'required|exists:roles,id',
        ]);

        // Encriptar contraseÃ±a (CRÃTICO)
        $data['password'] = bcrypt($data['password']);

        // Crear usuario
        $user = User::create($data);

        // Asignar rol
        $user->roles()->attach($data['role_id']);

        // Obtener el rol seleccionado
        $role = Role::find($data['role_id']);

        // ðŸ”¥ SI ES DOCTOR â†’ CREAR REGISTRO EN TABLA DOCTORS
        if ($role && $role->name === 'Doctor') {
            // Evita duplicados si ya existe
            if (!$user->doctor) {
                $user->doctor()->create([
                    'speciality_id' => null,
                    'medical_license_number' => null,
                    'biography' => null,
                ]);
            }
        }

        // ðŸ”¥ SI ES PACIENTE â†’ CREAR REGISTRO EN TABLA PATIENTS
        if ($role && $role->name === 'Paciente') {
            if (!$user->patient) {
                $patient = $user->patient()->create([]);

                session()->flash('swal', [
                    'icon' => 'success',
                    'title' => 'Usuario creado',
                    'text' => 'Paciente creado exitosamente.',
                ]);

                return redirect()->route('admin.patients.edit', $patient);
            }
        }

        session()->flash('swal', [
            'icon' => 'success',
            'title' => 'Usuario creado',
            'text' => 'El usuario ha sido creado exitosamente.',
        ]);

        return redirect()->route('admin.users.index')->with('success', 'Usuario creado exitosamente.');
    }

    /**
     * Muestra el formulario para editar un usuario existente.
     */
    public function edit(User $user)
    {
        $roles = Role::all();
        return view('admin.users.edit', compact('user', 'roles'));
    }

    /**
     * Actualiza un usuario existente
     */
    public function update(Request $request, User $user)
    {
        $data = $request->validate([
            'name' => 'required|string|min:3|max:255',
            'email' => 'required|string|email|unique:users,email,' . $user->id,
            'id_number' => 'required|string|min:5|max:20|regex:/^[A-Za-z0-9\-]+$/|unique:users,id_number,' . $user->id,
            'phone' => 'required|digits_between:7,15',
            'address' => 'required|string|min:3|max:255',
            'role_id' => 'required|exists:roles,id',
        ]);

        // Actualizar datos bÃ¡sicos
        $user->update($data);

        // Actualizar contraseÃ±a solo si se envÃ­a
        if ($request->filled('password')) {
            $user->password = bcrypt($request->password);
            $user->save();
        }

        // Sincronizar rol
        $user->roles()->sync([$data['role_id']]);

        // Obtener nuevo rol
        $role = Role::find($data['role_id']);

        // ðŸ”¥ SI CAMBIÃ“ A DOCTOR Y NO EXISTE DOCTOR â†’ CREARLO
        if ($role && $role->name === 'Doctor') {
            if (!$user->doctor) {
                $user->doctor()->create([
                    'speciality_id' => null,
                    'medical_license_number' => null,
                    'biography' => null,
                ]);
            }
        }

        // ðŸ”¥ SI CAMBIÃ“ A PACIENTE Y NO EXISTE PATIENT â†’ CREARLO
        if ($role && $role->name === 'Paciente') {
            if (!$user->patient) {
                $user->patient()->create([]);
            }
        }

        session()->flash('swal', [
            'icon' => 'success',
            'title' => 'Usuario actualizado',
            'text' => 'El usuario ha sido actualizado exitosamente.',
        ]);

        return redirect()->route('admin.users.index')->with('success', 'Usuario actualizado exitosamente.');
    }

    /**
     * Elimina un usuario
     */
    public function destroy(User $user)
    {
        // No permitir que un usuario se elimine a sÃ­ mismo
        if (Auth::user()->id === $user->id) {
            session()->flash('swal', [
                'icon' => 'error',
                'title' => 'Usuario no eliminado',
                'text' => 'No puedes eliminarte a ti mismo.',
            ]);
            abort(403, 'No puedes eliminarte a ti mismo.');
        }

        // Eliminar roles asociados
        $user->roles()->detach();

        // Eliminar usuario
        $user->delete();

        session()->flash('swal', [
            'icon' => 'success',
            'title' => 'Usuario eliminado',
            'text' => 'El usuario ha sido eliminado exitosamente.',
        ]);

        return redirect()->route('admin.users.index')->with('success', 'Usuario eliminado exitosamente.');
    }
}